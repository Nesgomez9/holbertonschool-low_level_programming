#include "3-calc.h"
#include <stdio.h>
/**
 * op_ad - Operation
 * @a: int
 * @b: int
 *
 * Return:  Operation
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Operation
 * @a: int
 * @b: int
 *
 * Return:  Operation
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Operation
 * @a: int
 * @b: int
 *
 * Return:  Operation
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Operation
 * @a: int
 * @b: int
 *
 * Return: Operation
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Operation
 * @a: int
 * @b: int
 *
 * Return: Operation
 */
int op_mod(int a, int b)
{
	return (a % b);
}
